#include <iostream>
using namespace std;

int main() {
	
double x=1.2300;   //zeros do not appear in output
cout<<x<<"\n";

double y=1567.56732;   //round of to total 6 digits 
cout<<y<<"\n";

double z=1244567.45;        //this will appear in exponent form and mantissa will get rounded off
cout<<z<<"\n";

double w=124456.67;           //this will round off
cout<<w<<"\n";

double u=123e+2;        //this will print in number form
cout<<u<<"\n";              


}
